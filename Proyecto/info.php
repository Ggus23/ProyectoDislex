				<h2>¿Que es la dislexia?</h2>
				<p>La dislexia es un trastorno del aprendizaje que afecta principalmente
					la capacidad de una persona para leer con fluidez y precisión.
					Es un trastorno neurobiológico que puede presentarse en niños y
					persistir hasta la edad adulta. Aunque la dislexia no está relacionada
					con la inteligencia, puede dificultar
					el rendimiento académico y la adquisición de habilidades de lectura y escritura.</p>

				<h2>Puntos Clave sobre la dislexia</h2>
				<p>Síntomas: Los síntomas de la dislexia pueden variar, pero los más comunes incluyen dificultades para
					reconocer y decodificar palabras, problemas con la ortografía, lentitud al leer, dificultades para
					recordar información leída y dificultades para comprender lo que se lee.
					<br>
					Causas: La dislexia tiene una base neurobiológica y puede ser heredada. Se cree que hay diferencias
					en la estructura y funcionamiento del cerebro que afectan la forma en que las personas con dislexia
					procesan el lenguaje escrito.
					<br>
					Diagnóstico: El diagnóstico de la dislexia generalmente se realiza por un profesional de la salud,
					como un psicólogo o un especialista en educación, a través de una evaluación exhaustiva que incluye
					pruebas de habilidades de lectura, escritura y procesamiento del lenguaje.
					<br>
					Tratamiento: No existe una cura para la dislexia, pero con el apoyo adecuado y estrategias de
					intervención, las personas con dislexia pueden aprender a leer y escribir con éxito. Los enfoques de
					tratamiento pueden incluir terapia educativa especializada, programas de intervención temprana,
					apoyo emocional y adaptaciones en el entorno educativo.
					<br>
					Apoyo y conciencia: Es importante brindar apoyo emocional y educativo a las personas con dislexia.
					La conciencia y comprensión de la dislexia por parte de los maestros, padres y la sociedad en
					general pueden ayudar a crear un entorno inclusivo que fomente el éxito de las personas con
					dislexia.</p>
