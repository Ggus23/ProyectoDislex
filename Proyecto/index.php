      <h2>¿Qué es la dislexia?</h2>
      <p>La dislexia es un trastorno del aprendizaje que afecta la capacidad de leer, escribir y comprender el
        lenguaje. Se caracteriza por dificultades en el reconocimiento preciso y/o fluido de palabras, así como por
        problemas de decodificación y ortografía.</p>

      <img src="assets/img/dislexia1.jfif" alt="Imagen 1" class="image1">

      <h2>Nuestro sistema para niños con dislexia</h2>
      <p>Nuestro sistema web está diseñado para ayudar a niños con dislexia a mejorar sus habilidades de lectura y
        escritura. A través de prácticas interactivas y personalizadas, los niños podrán desarrollar estrategias y
        técnicas específicas recomendadas por profesionales especializados en el tratamiento de la dislexia.</p>

      <img src="assets/img/DISLEXIA2.jpg" alt="Imagen 2" class="image2">

      <h2>Nuestros profesionales</h2>
      <p>Un buen profesional especializado en el tratamiento de la dislexia es aquel que posee un profundo conocimiento
        de este trastorno del aprendizaje y está comprometido en brindar un apoyo integral a los niños que lo padecen.
        Este profesional tiene habilidades para evaluar las dificultades específicas de cada niño, desarrollar
        estrategias y técnicas adaptadas a sus necesidades individuales, y proporcionar un ambiente de aprendizaje
        positivo y motivador. Además, un buen profesional trabaja en colaboración con los padres, los maestros y otros
        especialistas para crear un enfoque terapéutico multidisciplinario que facilite el progreso y el bienestar
        general del niño con dislexia.</p>

