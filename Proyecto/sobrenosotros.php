            entonces la empresa Dislexnet se encargo de cubrir esa necesidad de aquellos niños que querian mejorar su dislexia y aprender en linea esta plataforma esta implementada con una IA, <br>
        la cual ayuda a resolver las practicas subidas por los profesionales </p>
